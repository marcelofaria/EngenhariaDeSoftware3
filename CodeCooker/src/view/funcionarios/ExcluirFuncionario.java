    public void excluirFuncionario(int a){
        List<Usuario> usu;
        String nomeFunc, set;
        UsuarioDAO u = UsuarioDAO.getInstance();
        ConfirmaExcluirFuncionario cef = new ConfirmaExcluirFuncionario();
        
        lstFuncionarios.setSelectedIndex(a);      
        nomeFunc = lstFuncionarios.getSelectedValue();
        //System.out.println(nomeFunc);
        usu = u.retrieveLike(nomeFunc);

        //System.out.println(usu.get(0).getNome());
        
        cef.setVisible(true);
            
        for(int i = 0; i < usu.size(); i++){
            
            set = cef.getFuncionarios(nomeFunc, usu.get(i).getCpf());
            cef.setList(set);
        }     
    
    }
