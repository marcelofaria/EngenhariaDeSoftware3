    public Integer getCargoFunc(){
        if(String.valueOf(cmbCargo.getSelectedItem()).compareTo("Caixa") == 0){
            return 1;
        }
        else if(String.valueOf(cmbCargo.getSelectedItem()).compareTo("Metre") == 0){
            return 2;
        }
        else {
            return 3;
        }
    }
    
