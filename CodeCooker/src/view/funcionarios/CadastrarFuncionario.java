    public String getCPF(){
        
        String cpf;
        String[] cpfTratado;
        
        cpf = txtCPF.getText();
        
        //System.out.println(CPF);
        cpfTratado = cpf.split("\\.");
        
        String digito = cpfTratado[2];
        String[] digitoE = digito.split("-");
        
        String CPFinal = cpfTratado[0] + cpfTratado[1] + digitoE[0] + digitoE[1];

        return CPFinal;
    }
    
